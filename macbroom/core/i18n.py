def normalize_lang(value: str | None) -> str:
    """Normalize browser/Accept-Language values to ``zh`` or ``en``."""
    if not value:
        return DEFAULT_LANG
    token = value.split(",", 1)[0].strip().lower()
    if token.startswith("zh"):
        return "zh"
    if token.startswith("en"):
        return "en"
    return DEFAULT_LANG


def tr(lang: str | None, key: str, **kwargs) -> str:
    """Translate a key with fallback to English, then Chinese, then key."""
    normalized = normalize_lang(lang)
    text = (
        TRANSLATIONS.get(normalized, {}).get(key)
        or TRANSLATIONS["en"].get(key)
        or TRANSLATIONS["zh"].get(key)
        or key
    )
    return text.format(**kwargs) if kwargs else text
